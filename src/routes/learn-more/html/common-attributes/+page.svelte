<h1>Загальні атрибути</h1>

<p>
	В HTML є кілька атрибутів, які є загальними для всіх елементів, такі атрибути звуться загальними,
	ґлобальними чи універсальними.
</p>

<p>
	Ці атрибути можуть використовуватися з усіма тегами, хоча на деякі з них вони можуть ніяк не
	впливати.
</p>

<p>Всі зазначені нижче атрибути є загальними і можуть бути вказані до будь-якого HTML елемента</p>

